import java.lang.String;

public class Person {
  private String Name;

  public String getName() {
    return Name;
  }

  public void setName(String Name) {
    this.Name = Name;
  }

  private String Age;

  public String getAge() {
    return Age;
  }

  public void setAge(String Age) {
    this.Age = Age;
  }
}